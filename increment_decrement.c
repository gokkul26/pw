//PRE-INCREMENT AND POST-INCREMENT
#include<stdio.h>
int main()
{
    int a=2;
    printf("\npre increment= %d",++a);
    printf("\nA : %d",a);
    printf("\npost increment= %d",a++); 
    printf("\nA : %d",a);
}


